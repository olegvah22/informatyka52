#include <iostream>
using namespace std;

int main() {
    int a = 52;
    int b = 2;

    cout << "Liczba a: " << a << endl;
    cout << "Liczba b: " << b << endl;
    cout << "Suma: " << a + b << endl;
    cout << "Roznica: " << a - b << endl;
    cout << "Iloczyn: " << a * b << endl;
    cout << "Reszta z dzielenia: " << a % b <<endl;

    return 0;
}
